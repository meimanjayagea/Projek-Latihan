// perelangan menggunakan for
for (let i = 0; i >= 5; i++){
    console.log("Saya berulang ke : " + i);
}

//perulangan menggunakan while
let m = 0;
while (m < 5) {
    console.log("saya berulang ke : " + m);
    m++;
}

